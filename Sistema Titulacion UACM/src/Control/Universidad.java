package Control;

import java.io.File;

public class Universidad 
{
	private String correrUniversidad;
	private String matricula;
	private String tiempoTesisDiario;
	private String colegio;
	private String carrera;
	private String plantel;
	private File historialAca;
	private File carta;
	
	public String getCorrerUniversidad() 
	{
		return correrUniversidad;
	}
	
	public void setCorrerUniversidad(String correrUniversidad)
	{
		this.correrUniversidad = correrUniversidad;
	}
	
	public String getMatricula() 
	{
		return matricula;
	}
	
	public void setMatricula(String matricula)
	{
		this.matricula = matricula;
	}
	
	public String getTiempoTesisDiario()
	{
		return tiempoTesisDiario;
	}
	
	public void setTiempoTesisDiario(String tiempoTesisDiario)
	{
		this.tiempoTesisDiario = tiempoTesisDiario;
	}
	
	public String getColegio() 
	{
		return colegio;
	}
	
	public void setColegio(String colegio)
	{
		this.colegio = colegio;
	}
	
	public String getCarrera() 
	{
		return carrera;
	}
	
	public void setCarrera(String carrera) 
	{
		this.carrera = carrera;
	}
	
	public String getPlantel()
	{
		return plantel;
	}
	
	public void setPlantel(String plantel) 
	{
		this.plantel = plantel;
	}
	
	public File getHistorialAca()
	{
		return historialAca;
	}
	
	public void setHistorialAca(File historialAca)
	{
		this.historialAca = historialAca;
	}
	
	public File getCarta() 
	{
		return carta;
	}
	
	public void setCarta(File carta)
	{
		this.carta = carta;
	}
	
	
	
	
}
